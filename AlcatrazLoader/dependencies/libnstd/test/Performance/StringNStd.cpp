
#include <nstd/String.hpp>
#include <cstdlib>

void testStringNStd(int iterations)
{
  srand(100);
  usize val = 0;
  String blah[] = {
    _T("12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678"),
    _T("12345688 12345688 12345688 12345688 12345688 12345688 12345688 12345688 12345688"),
    _T("12345698 12345698 12345698 12345698 12345698 12345698 12345698 12345698 12345698")
  };
  for (int i = 0; i < iterations; ++i)
  {
    if(String(_T("12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678")) == blah[rand() % 3])
      val += (String(_T("12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678 12345678")) + blah[rand() % 3]).length();
  }
}
